package org.telosys.doc.commons;

public class ItemLink {

	private final String text ;
	private final String page ;
	
	public ItemLink(String text, String page) {
		super();
		this.text = text;
		this.page = page;
	}
	
	public String getText() {
		return text;
	}
	
	public String getPage() {
		return page;
	}
	

}
